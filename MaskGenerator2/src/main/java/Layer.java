public class Layer {
    private String layerName;
    private int gdsNumber;

    public String getLayerName() {
        return layerName;
    }

    public void setLayerName(String layerName) {
        this.layerName = layerName;
    }

    public int getGdsNumber() {
        return gdsNumber;
    }

    public void setGdsNumber(int gdsNumber) {
        this.gdsNumber = gdsNumber;
    }
}
